#!/usr/bin/env python3
from __future__ import annotations

from pathlib import Path
import sys


ROOT = Path(__file__).resolve().parent

PRESETS = {
    "k3-stress-test": {
        "readme_parts": [
            "goals/default/README.md",
            "implementation/non-legacy-evaluator/README.md",
            "hints/canonical-potential/README.md",
            "hints/metrics/k3_stress_test/README.md",
        ],
        "env_parts": [
            "hints/metrics/k3_stress_test/.env",
        ],
    },
    "k4-general-task": {
        "readme_parts": [
            "goals/default/README.md",
            "implementation/non-legacy-evaluator/README.md",
            "hints/canonical-potential/README.md",
            "hints/metrics/k4_general_task/README.md",
        ],
        "env_parts": [
            "hints/metrics/k4_general_task/.env",
        ],
    },
}


def _read_trimmed(path: Path) -> str:
    return path.read_text(encoding="utf-8").strip()


def _join_readme(parts: list[str]) -> str:
    body = "\n\n".join(_read_trimmed(ROOT / part) for part in parts)
    return "<!-- Generated by tasks/build_preset.py. Do not edit by hand. -->\n\n" + body + "\n"


def _join_env(parts: list[str]) -> str:
    lines: list[str] = ["# Generated by tasks/build_preset.py. Do not edit by hand."]
    for part in parts:
        text = _read_trimmed(ROOT / part)
        if text:
            lines.extend(text.splitlines())
    return "\n".join(lines) + "\n"


def build_preset(name: str) -> None:
    if name not in PRESETS:
        available = ", ".join(sorted(PRESETS))
        raise SystemExit(f"Unknown preset '{name}'. Available presets: {available}")

    config = PRESETS[name]
    preset_dir = ROOT / name
    preset_dir.mkdir(parents=True, exist_ok=True)
    (preset_dir / "README.md").write_text(_join_readme(config["readme_parts"]), encoding="utf-8")
    (preset_dir / ".env").write_text(_join_env(config["env_parts"]), encoding="utf-8")
    print(f"Generated preset assets in {preset_dir}")


def main(argv: list[str]) -> int:
    names = argv[1:] or sorted(PRESETS)
    for name in names:
        build_preset(name)
    return 0


if __name__ == "__main__":
    raise SystemExit(main(sys.argv))
